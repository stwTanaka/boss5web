import React from "react";
import { NavLink } from "react-router-dom";
import { Nav } from "reactstrap";
// javascript plugin used to create scrollbars on windows
import PerfectScrollbar from "perfect-scrollbar";

import logo from "logo.svg";
import { Card, CardImg, CardBody, CardTitle, CardText } from 'reactstrap';
import {Button} from "reactstrap";
import Sample from './sample';



var ps;

const style = {
  width: "50%",
  margin: "0 auto",
  marginTop: 50
};

function Sidebar(props) {
  const sidebar = React.useRef();
  // verifies if routeName is the one active (in browser input)
  const activeRoute = (routeName) => {
    return props.location.pathname.indexOf(routeName) > -1 ? "active" : "";
  };

  

  React.useEffect(() => {
    if (navigator.platform.indexOf("Win") > -1) {
      ps = new PerfectScrollbar(sidebar.current, {
        suppressScrollX: true,
        suppressScrollY: false,
      });
    }
    return function cleanup() {
      if (navigator.platform.indexOf("Win") > -1) {
        ps.destroy();
      }
    };
  });

  

  return (
    <div
      className="sidebar"
      data-color={props.bgColor}
      data-active-color={props.activeColor}
    >
      <div className="logo">
        <a
          href="https://www.creative-tim.com"
          className="simple-text logo-mini"
        >
          <div className="logo-img">
            <img src={logo} alt="react-logo" />
          </div>
        </a>
        <a
          href="https://www.creative-tim.com"
          className="simple-text logo-normal"
        >
          Boss5Web
        </a>
        <Card style={{width: '15rem'}}>
    {/* <CardImg top classname = "avatar border-gray" src={require("assets/img/user.png").default} width="48" height="48" alt="..."/> */}
    
    <CardBody>
        <CardTitle><img class="avatar border-gray"  src={require("assets/img/user.png").default}></img>stw 一郎</CardTitle>          
        <card-text>develop@shred-stw.com</card-text>        
    </CardBody>
</Card>
      </div>

      <div className="sidebar-wrapper" ref={sidebar}>
      <div className="App">
      <div style={style}>
        <Sample />
      </div>
    </div>
        <Nav>
      

  
        
          {props.routes.map((prop, key) => {
            return (
              <li
                className={
                  activeRoute(prop.path) + (prop.pro ? " active-pro" : "")
                }
                key={key}
              >
             
                <NavLink
                  to={prop.layout + prop.path}
                  className="nav-link"
                  activeClassName="active"
                >
                  <i className={prop.icon} />
                  <p>{prop.name}</p>
                </NavLink>
              </li>
            );
          })}
        </Nav>
      </div>
    </div>
  );
}



export default Sidebar;